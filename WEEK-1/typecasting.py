# Typecasting: to change a datatype from one to another

a = int (10.1) # this will convert 10.1 to 10

print (a) # prints 10

b = float (10) # this will convert 10 to 10.0

print (b)

c = str(10.3) # this will convert 10.3 to '10.3'

print (c)

d = bool (0) # False

print (d)

e = bool (10) ## any non zero number, both positive or negative, is True

print (e)


g = bool ('India') # any non-empty string will pop out True

print (g)

h = bool ('') # since it is an empty string will pop out False

print (h)